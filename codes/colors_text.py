class TextColor:
    """Defining the color for printing text on console"""
    BOLD: str = '\033[1m'
    ENDC: str = '\033[0m'
    FAIL: str = '\033[91m'
    HEADER: str = '\033[95m'
    OKBLUE: str = '\033[94m'
    OKCYAN: str = '\033[96m'
    WARNING: str = '\033[93m'
    OKGREEN: str = '\033[92m'
    UNDERLINE: str = '\033[4m'
